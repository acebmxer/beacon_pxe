"""Generate the single boot.ipxe menu served to both BIOS and UEFI clients.

Modern look: a colour scheme (always supported) plus an optional background PNG
(best-effort; the menu still works if the iPXE build lacks PNG/console support).
"""
from sqlalchemy import select
from sqlalchemy.orm import Session

from .. import config
from ..models import Image
from ..store import all_settings


def _header(server_ip: str, title: str) -> str:
    host = server_ip or "${next-server}"
    host_url = f"http://{host}"
    # Every styling line is guarded with `|| goto styled` so that on any iPXE
    # build lacking a feature (PNG console, colour cmds) the script still falls
    # through to the menu instead of aborting.
    return f"""#!ipxe
# GENERATED by the PXE web app. Do not edit by hand.

set boot-url {host_url}
# Used by NFS-netboot images: kernel cmdline references ${{server-ip}} for nfsroot.
set server-ip {host}
set menu-title {title}

# --- Colour scheme (always applied; || true so a missing feature doesn't abort)
colour --rgb 0x1a1d29 0 || true
colour --rgb 0x00d2ff 4 || true
cpair --foreground 7 --background 4 1 || true
cpair --foreground 6 --background 0 0 || true
# Background image is best-effort; failure must not abort the script.
console --picture {host_url}/background.png || true
"""


MENU_TOP = """
:start
menu ${menu-title}
item --gap -- ----------------------- Operating Systems -----------------------
"""

MENU_FOOTER = """item --gap -- ----------------------------------------------------------------
item shell        iPXE shell
item reboot       Reboot
item exit_ipxe    Continue local boot
choose --default {default} --timeout 30000 target && goto ${target}

:shell
echo Type 'exit' to return to the menu.
shell
goto start

:reboot
reboot

:exit_ipxe
exit
"""


def _image_entries(images: list[Image]) -> tuple[str, str]:
    """Return (menu items, boot labels) for ready, enabled images."""
    items = []
    labels = []
    for img in images:
        tag = f"os_{img.id}"
        items.append(f"item {tag}        {img.name}")
        base_url = "${boot-url}"
        args = img.boot_args or ""
        if img.os_family == "xcpng":
            # iPXE's EFI build has NO multiboot support, so UEFI clients can't
            # boot Xen directly — they chainload the ISO's grubx64.efi, which
            # does Multiboot2 under EFI via the generated /EFI/xenserver/grub.cfg.
            # BIOS clients (undionly.kpxe) still multiboot xen.gz directly.
            xen_dir = img.kernel_path.rsplit("/", 1)[0]
            xen = f"{base_url}/{xen_dir}/xen.gz"
            vmlinuz = f"{base_url}/{img.kernel_path}"
            initrd = f"{base_url}/{img.initrd_path}"
            labels.append(
                f":{tag}\n"
                f"iseq ${{platform}} efi && goto {tag}_efi || goto {tag}_bios\n"
                f":{tag}_efi\n"
                f"echo Chainloading GRUB for {img.name} (UEFI) ...\n"
                f"chain {base_url}/EFI/xenserver/grubx64.efi || goto start\n"
                f":{tag}_bios\n"
                f"echo Booting {img.name} ...\n"
                f"kernel {xen} dom0_max_vcpus=1-2 dom0_mem=2048M,max:2048M console=vga || goto start\n"
                f"module {vmlinuz} {args} || goto start\n"
                f"module {initrd} || goto start\n"
                f"boot || goto start\n"
            )
        else:
            kernel = f"{base_url}/{img.kernel_path}"
            initrd = f"{base_url}/{img.initrd_path}"
            labels.append(
                f":{tag}\n"
                f"echo Booting {img.name} ...\n"
                f"kernel {kernel} {args} || goto start\n"
                f"initrd {initrd} || goto start\n"
                f"boot || goto start\n"
            )
    return "\n".join(items), "\n".join(labels)


def _grub_cfg(images: list[Image], server_ip: str) -> str | None:
    """GRUB config for UEFI XCP-NG clients chainloaded to grubx64.efi.

    Paths are absolute against GRUB's root, which is the HTTP server iPXE chained
    from, so /os/<id>/... resolves to the same files BIOS clients multiboot.
    ${server-ip} in boot_args is an iPXE variable GRUB can't expand, so it is
    substituted with the configured server IP here.
    """
    xcpng = [img for img in images if img.os_family == "xcpng"]
    if not xcpng:
        return None
    entries = []
    for img in xcpng:
        xen_dir = img.kernel_path.rsplit("/", 1)[0]
        args = (img.boot_args or "").replace("${server-ip}", server_ip)
        entries.append(
            f'menuentry "{img.name}" {{\n'
            f"  multiboot2 /{xen_dir}/xen.gz dom0_max_vcpus=1-2 dom0_mem=2048M,max:2048M console=vga\n"
            f"  module2 /{img.kernel_path} {args}\n"
            f"  module2 /{img.initrd_path}\n"
            f"}}\n"
        )
    # One image: boot it immediately (iPXE already made the choice).  Several:
    # show GRUB's own menu briefly to disambiguate.
    timeout = 0 if len(xcpng) == 1 else 10
    return (
        "# GENERATED by the PXE web app. Do not edit by hand.\n"
        f"set timeout={timeout}\n"
        "set default=0\n\n"
        + "\n".join(entries)
    )


def render(db: Session) -> str:
    settings = all_settings(db)
    server_ip = settings.get("server_ip", "")
    title = settings.get("menu_title", "Beacon")

    images = db.execute(
        select(Image).where(Image.status == "ready", Image.enabled == 1)
        .order_by(Image.name)
    ).scalars().all()

    items, labels = _image_entries(images)
    if not items:
        items = "item --gap -- (no images uploaded yet — add one in the web UI)"

    default = f"os_{images[0].id}" if images else "exit_ipxe"

    text = (
        _header(server_ip, title)
        + MENU_TOP
        + items + "\n"
        + MENU_FOOTER.replace("{default}", default)
        + "\n"
        + labels
    )

    out = config.BOOTROOT_DIR / "boot.ipxe"
    out.write_text(text)

    grub = _grub_cfg(images, server_ip)
    if grub is not None:
        grub_out = config.BOOTROOT_DIR / "EFI" / "xenserver" / "grub.cfg"
        grub_out.parent.mkdir(parents=True, exist_ok=True)
        grub_out.write_text(grub)

    return text
